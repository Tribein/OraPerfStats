CREATE TABLE oradb.sqlstats ( 
dbuniquename String,  
hostname String,
snapTime DateTime,
sql_id FixedString(13),
plan_hash_value UInt64,
version_count UInt32,
sharable_mem UInt64,
persistent_mem UInt64,
runtime_mem UInt64,
sorts UInt32,
loaded_versions UInt32,
open_versions UInt32,
users_opening UInt32,
users_executing UInt32,
fetches UInt64,
executions UInt64,
px_servers_executions UInt64,
end_of_fetch_count UInt64,
loads UInt32,
first_load_time DateTime,
last_load_time DateTime,
last_active_time DateTime,
invalidations UInt64,
parse_calls UInt64,
disk_reads UInt64,
direct_writes UInt64,
buffer_gets UInt64,
cpu_time UInt64,
elapsed_time UInt64,
application_wait_time UInt64,
concurrency_wait_time UInt64,
cluster_wait_time UInt64,
user_io_wait_time UInt64,
plsql_exec_time UInt64,
java_exec_time UInt64,
rows_processed UInt64,
command_type UInt16,
optimizer_cost Decimal(36,0),
parsing_schema_name String,
kept_versions UInt32,
object_status String,
sql_profile String,
program_id UInt32,
program_line# UInt32,
io_cell_offload_eligible_bytes UInt64,
io_interconnect_bytes UInt64,
physical_read_requests UInt64,
physical_read_bytes UInt64,
physical_write_requests UInt64,
physical_write_bytes UInt64,
optimized_phy_read_requests UInt64,
io_cell_uncompressed_bytes UInt64,
io_cell_offload_returned_bytes UInt64
) ENGINE = MergeTree() PARTITION BY toYYYYMM(snapTime) ORDER BY (dbuniquename,snapTime) SETTINGS index_granularity=8192;
CREATE TABLE oradb.sqlstats_buffer AS oradb.sqlstats ENGINE = Buffer('oradb', 'sqlstats', 16, 14400, 86400, 100000, 1000000, 100000000, 1000000000);
